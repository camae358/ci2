// function to create image elements for destiny
function createDestinyImageElements() {
    destinyImages.forEach((image, index) => {
        const img = document.createElement('div');
        img.style.backgroundImage = `url(${image.img})`;
        img.style.backgroundSize = 'cover';
        img.style.backgroundPosition = 'center';
        img.style.width = '100%';
        img.style.height = '100%';
        img.style.borderRadius = '15px';

        const box = document.querySelector(`.destiny_container .box:nth-child(${index + 1})`);
        box.innerHTML = '';
        box.appendChild(img);
    });
}
// call the function to create destiny images
